package oit.is.team7.schedule.model;

public class Group {
    private int groupid;
    private String groupname;

    public int getGroupid() {
        return this.groupid;
    }

    public void setGroupid(int id) {
        this.groupid = id;
    }

    public String getGroupname() {
        return this.groupname;
    }

    public void setGroupname(String name) {
        this.groupname = name;
    }
}
